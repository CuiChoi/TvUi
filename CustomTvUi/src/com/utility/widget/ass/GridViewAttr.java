package com.utility.widget.ass;

public class GridViewAttr extends IGroupAttr {

	/**
	 * 列数
	 */
	public int columns;
	
	/**
	 * 列间距，使用视觉值，忽略外发光
	 */
	public int columnGap;
	
	/**
	 * 行间距，使用视觉值，忽略外发光
	 */
	public int rowGap;
	
	/**
	 * 左侧内边距，使用视觉值，忽略外发光
	 */
	public int paddingLeft;
	
	/**
	 * 上侧内边距，使用视觉值，忽略外发光
	 * 顶部安全距离也使用此值
	 */
	public int paddingTop;
	
	/**
	 * 右侧内边距，使用视觉值，忽略外发光
	 */
	public int paddingRight;
	
	/**
	 * 下侧内边距，使用视觉值，忽略外发光
	 * 底部安全距离也使用此值
	 */
	public int paddingBottom;
	
}
